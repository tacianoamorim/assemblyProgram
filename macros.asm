############################################################################
# PRINT INT
############################################################################
.macro print_int (%x)
	li $v0, 1
	add $a0, $zero, %x
	syscall
.end_macro

############################################################################
# PRINT STRING
############################################################################
.macro print_str (%str)
.data
	myLabel: .asciiz %str
.text
	li $v0, 4
	la $a0, myLabel
	syscall
.end_macro

############################################################################
# PRINT STRING VAR
############################################################################
.macro print_var_str ()
.text
	li $v0, 4
	syscall
.end_macro
